# 선술 연산자(arithmetic operator) : 수치형 데이터를 가지고 진행하는 일반 산술 연산
# +, -, *, /, %, **, //
print(3 + 5.2, 3.2 + 5.3)

print(13 + 5)
print(13 - 5)
print(13 * 5)
print(13 / 5)  # 나눗쎔(나머지 발생)
print(13 % 5)  # 나머지 연산
print(13 // 5)  # 몫 계산
print(13 ** 5)  # 제곱

# 비트 연산(bitwise operator)
# 이진수 단위로 연산을 수행하는 연산자
# &(and), |(or), ^(xor), ~(not), <<(left shift), >>(right shift)
print(3 & 5, 3 | 3, 3 ^ 5, ~3, 3 << 1, 3 >> 1)
print(3 & 5)    # AND 연산: 3(011) & 5(101) => 001 => 1
print(3 | 3)    # OR 연산: 3(011) | 3(011) => 011 => 3
print(3 ^ 5)    # XOR 연산: 3(011) ^ 5(101) => 110 => 6
print(~3)       # NOT 연산: ~3 => -(3+1) => -4
print(3 << 1)   # 왼쪽 시프트: 3(011)을 왼쪽으로 1비트 이동 => 6
print(3 >> 1)   # 오른쪽 시프트: 3(011)을 오른쪽으로 1비트 이동 => 1

# 대입 연산자(assignment operator)
# 변수에 값을 저장하거나 기존 값을 갱신할 때 사용
# =, +=, -=, *=, /=, %=, //=, **=, &=, |=, ~=, ^=, >>=, <<=
d = 10      # d에 10 할당
d += 3      # d = d + 3 => d는 13
d /= 4      # d = d / 4 => d는 13 / 4 => 3.25
print(d)